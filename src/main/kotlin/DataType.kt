fun main() {
    // Tipe data bilangan bulat
    val byteValue: Byte = 42
    val intValue: Int = 12345
    val longValue: Long = 9876543210

    // Tipe Data Pecahan
    val floatValue: Float = 3.14f
    val doubleValue: Double = 2.71828

    // Tipe Data Karakter
    val charValue: Char = 'A'

    // Tipe Data Boolean
    val isTrue: Boolean = true
    val isFalse: Boolean = false

    // Tipe Data String
    val greeting: String = "Hello, Kotlin!"

    // Tipe Data Collections
    val myList: List<String> = listOf("apple", "banana", "orange")
    val mySet: Set<Int> = setOf(1, 2, 3, 4, 5)
    val myMap: Map<String, Int> = mapOf("one" to 1, "two" to 2, "three" to 3)
}